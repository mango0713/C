#include <stdio.h>

main()
{
	// 주석문
	/* 주석문 */
	int a = 18;      // int integer interger  정수 (양수 정수 0)
	int b = 7; 
	printf ("%d+%d = %d \n", a, b, a+b);    //d decimal  십진수 
	printf ("%d-%d = %d \n", a, b, a-b);
	printf ("%d*%d = %d \n", a, b, a*b);
	printf ("%d/%d = %.200lf \n", a, b, (double)a/b);
}
